import Cookies from "js-cookie";

export const locationCurencyValue = async () => {
  const cached = Cookies.get("location_currency_info");
  if (cached) {
    try {
      const parsed = JSON.parse(cached); // parse the stringified cookie
      const isFresh = Date.now() - parsed.timestamp < 2 * 60 * 60 * 1000; // 2 hours

      if (isFresh) {
        return parsed.data;
      }
    } catch (err) {
      console.error("Failed to parse cached currency data", err);
    }
  }
  const key =
    process.env.NEXT_PUBLIC_IPREGISTRY_KEY ||
    "ira_BvHojOHZv2sNzum5Z7qhpvza7LflJS3RgPQY";
  try {
    const [currencyInfoResponse, currencyInrConversionResponse] =
      await Promise.all([
        fetch(`https://api.ipregistry.co/?key=${key}`),
        fetch("https://open.er-api.com/v6/latest/INR"),
      ]);
    if (currencyInfoResponse.ok && currencyInrConversionResponse.ok) {
      const currencyInfo = await currencyInfoResponse.json();
      const currencyInrConversion = await currencyInrConversionResponse.json();

      const currencyCode = currencyInfo.currency.code;
      const currencySymbol = currencyInfo.currency.symbol;
      const countryCode = currencyInfo.location.country.code;
      const currencyValue = currencyInrConversion.rates[currencyCode];

      const data = {
        currencyCode,
        currencySymbol,
        currencyValue: currencyValue || 0,
        countryCode,
      };

      // Set cookie with timestamp and data
      Cookies.set(
        "location_currency_info",
        JSON.stringify({ data, timestamp: Date.now() }),
        { expires: 1 },
      );

      return data;
    }
  } catch (err) {
    console.error("Currency fetch failed", err);
  }

  // Fallback
  return { currencyCode: "INR", currencySymbol: "₹", currencyValue: 0 };
};
